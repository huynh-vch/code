#include <iostream>
using namespace std;

int main()
{
	int a = 0, b = 0;
	cout << "CHUONG TRINH TINH TOAN" << endl;
	cout << "Nhap A = " << endl;
	cin >> a;
	cout << "Nhap B = " << endl;
	cin >> b;
	cout << "a = " << a << endl;
	cout << "b = " << b << endl;
	cout << "Tong A + B = " << a + b << endl;
	cout << "Hieu A - B = " << a - b << endl;
	cout << "Tich A * B = " << a * b << endl;
	cout << "Thuong A / B = " << a / b << endl; //chia lay phan nguyen
	cout << "Chia du A % B =" << a % b << endl; //chia lay phan du

	return 0;
}